class AlternateYesAndNo
{
	public static void main(String []args)
	{
		//Repeat 10 times
		for(int _counter = 1; _counter <= 10; _counter++)
		{
			//if _counter % 2 = 0
			if(_counter % 2 == 0)
				//output Yes
				System.out.println("Yes");
			//else
			else
				//output No
				System.out.println("No");
		}
	}
}